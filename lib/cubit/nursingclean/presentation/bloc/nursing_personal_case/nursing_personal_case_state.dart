import 'package:equatable/equatable.dart';
import 'package:m2health/cubit/nursingclean/domain/entities/nursing_case.dart';

abstract class NursingPersonalCaseState extends Equatable {
  const NursingPersonalCaseState();

  @override
  List<Object> get props => [];
}

class NursingPersonalCaseInitial extends NursingPersonalCaseState {}

class NursingPersonalCaseLoading extends NursingPersonalCaseState {}

class NursingPersonalCaseLoaded extends NursingPersonalCaseState {
  final List<NursingCase> cases;

  const NursingPersonalCaseLoaded(this.cases);

  @override
  List<Object> get props => [cases];
}

class NursingPersonalCaseError extends NursingPersonalCaseState {
  final String message;

  const NursingPersonalCaseError(this.message);

  @override
  List<Object> get props => [message];
}
